//JS tiene objetos globales que son herramientas que podemos introducir a nuestro código.
//Por ejemplo el objeto "console" y su método "log".
//Otro objeto es "Math", que se usa para hacer cálculos.

Math.pow(2,4); //Sirve para hacer potencias. 2 elevado a la 4.

Math.round(0.49); //redonde al mas cerca. X.50 se redondea para arriba.
Math.floor; //redondea para abajo.
Math.ceil; //redondea para arriba.

Math.max(1,3,5,7) //arroja el número más alto de un conjunto.
Math.min(1,3,5,7) //arroja el número menor.

Math.random(); //genera un número aleatorio decimal entre 0 y 1.
